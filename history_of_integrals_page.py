
import streamlit as st

def history_of_integrals_page():
    st.header("History of Integrals")
    st.write("""
    The history of integrals can be traced to ancient mathematics, where early attempts to calculate areas and volumes 
    were made. In ancient Egypt, the Rhind Mathematical Papyrus included early examples of approximations for areas of 
    curved shapes.

    In the 3rd century BCE, Archimedes developed methods to calculate areas and volumes using a method resembling 
    integration. However, it wasn't until the 17th century that the modern concept of integration was developed 
    independently by Isaac Newton and Gottfried Wilhelm Leibniz.

    Newton and Leibniz's work laid the foundation for calculus, with integration becoming the inverse operation 
    of differentiation. Their work unified the geometric interpretation of integration (areas and volumes) with 
    algebraic methods.

    Later, 19th-century mathematicians such as Riemann and Lebesgue further refined the concept, leading to 
    Riemann integration and Lebesgue integration, which are still fundamental in mathematics today.
    """)
